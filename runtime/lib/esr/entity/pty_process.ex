defmodule Esr.Entity.PtyProcess do
  @moduledoc """
  Generic PTY-backed peer (PR-22, 2026-05-01). Owns one OS process
  spawned via erlexec's `:pty` wrapper. Fans raw stdout chunks to
  Phoenix.PubSub topic `"pty:<session_id>"` for `EsrWeb.AttachLive`
  subscribers; accepts stdin via the public `write/2` and SIGWINCH via
  `resize/3`.

  cc_process is **not** a PubSub subscriber here — the conversation path
  is cc_mcp → `cli:channel/<sid>`. PtyProcess only serves the operator-
  facing browser attach.

  ## Process structure

  - `start_link/1` starts `__MODULE__.OSProcessWorker` (generated by the
    `use Esr.OSProcess` macro). The worker holds the GenServer mailbox.
  - `init/1` runs inside the worker and registers under
    `Esr.Entity.Registry` with the binary actor_id `"pty:<sid>"` so
    `EsrWeb.AttachLive` can find this peer to forward stdin/resize.
  - `on_raw_stdout/2` (new optional `Esr.OSProcess` callback) fires
    BEFORE line-splitting, broadcasting raw bytes so ANSI escapes
    spanning chunk boundaries reach xterm.js intact.
  - `on_terminate/1` broadcasts a bare `:pty_closed` so attached
    LiveViews can render a "session ended" overlay.

  M-2.4: PR-21ω' `rewire_session_siblings/1` + `patch_neighbor_in_state/3`
  (which used `:sys.replace_state/2` to patch sibling peers'
  `state.neighbors` with this PTY's pid post-init) deleted. ActorQuery
  (M-1) replaces neighbor lookup; PTY is registered in Index 3 under
  `(session_id, :pty_process)` in init, so peers resolve us via
  `Esr.ActorQuery.list_by_role/2` without any patching ceremony.
  """

  require Logger

  @behaviour Esr.Role.State

  use Esr.Entity.Stateful
  use Esr.OSProcess, kind: :pty, wrapper: :pty

  alias Phoenix.PubSub

  # M-1.5: compile-time role constant. Consumed by `register_attrs/2`
  # in init/1 below and by `deregister_attrs/2` in terminate/2 so a
  # rename happens in exactly one place. `Esr.ActorQuery.list_by_role/2`
  # callers (M-2+) match against this atom.
  @role :pty_process

  # ------------------------------------------------------------------
  # Boot
  # ------------------------------------------------------------------

  def start_link(args) do
    args = Map.put_new(args, :subscriber, self())
    GenServer.start_link(__MODULE__.OSProcessWorker, args, name: name_for(args))
  end

  def child_spec(args) do
    %{
      id: __MODULE__,
      start: {__MODULE__, :start_link, [args]},
      type: :worker,
      restart: :transient,
      shutdown: 5_000
    }
  end

  @impl Esr.Entity
  def spawn_args(params) do
    name = "esr_pty_#{:erlang.unique_integer([:positive])}"

    %{
      session_name: name,
      dir: Esr.Entity.get_param(params, :dir) || "/tmp",
      session_id: Esr.Entity.get_param(params, :session_id),
      workspace_name: Esr.Entity.get_param(params, :workspace_name),
      chat_id: Esr.Entity.get_param(params, :chat_id),
      app_id: Esr.Entity.get_param(params, :app_id),
      start_cmd: Esr.Entity.get_param(params, :start_cmd)
    }
  end

  # ------------------------------------------------------------------
  # Public API (called from EsrWeb.AttachLive event handlers)
  # ------------------------------------------------------------------

  @doc "Forward keystrokes from xterm.js to the wrapped process's stdin."
  @spec write(String.t(), iodata()) :: :ok | {:error, term()}
  def write(sid, data) when is_binary(sid) do
    # DIAGNOSTIC (temporary 2026-05-02): log every stdin write to confirm
    # the send_input path actually reaches the PTY worker.
    require Logger
    bytes = IO.iodata_to_binary(data)
    digest = bytes |> :binary.bin_to_list() |> Enum.filter(&(&1 in 32..126 or &1 == 10)) |> List.to_string() |> String.slice(0, 200)
    Logger.info("pty.write sid=#{sid} bytes=#{byte_size(bytes)} digest=#{inspect(digest)}")

    case Esr.Entity.Registry.lookup("pty:" <> sid) do
      {:ok, worker_pid} ->
        __MODULE__.OSProcessWorker.write_stdin(worker_pid, data)

      :error ->
        Logger.warning("pty.write sid=#{sid} no PTY worker registered")
        {:error, :no_pty_for_session}
    end
  end

  @doc "Forward window-size change from xterm.js (SIGWINCH)."
  @spec resize(String.t(), pos_integer(), pos_integer()) :: :ok | {:error, term()}
  def resize(sid, cols, rows)
      when is_binary(sid) and is_integer(cols) and is_integer(rows) and cols > 0 and rows > 0 do
    case Esr.Entity.Registry.lookup("pty:" <> sid) do
      {:ok, worker_pid} -> GenServer.cast(worker_pid, {:winsz, cols, rows})
      :error -> {:error, :no_pty_for_session}
    end
  end

  # ------------------------------------------------------------------
  # Peer init — registers in Entity.Registry (Index 1/2/3); ActorQuery
  # is now the only neighbor lookup mechanism.
  # ------------------------------------------------------------------

  def init(%{session_name: _, dir: _} = args) do
    state = %{
      session_name: args.session_name,
      dir: args.dir,
      subscribers: [args[:subscriber] || self()],
      session_id: Map.get(args, :session_id),
      workspace_name: Map.get(args, :workspace_name),
      chat_id: Map.get(args, :chat_id),
      app_id: Map.get(args, :app_id),
      start_cmd: Map.get(args, :start_cmd)
    }

    sid = state.session_id

    # Register so AttachLive can find this peer by sid. Best-effort —
    # in unit tests we may run without a registered Entity.Registry; the
    # rest of the peer still works without registration.
    if is_binary(sid) and sid != "" do
      _ =
        try do
          Esr.Entity.Registry.register("pty:" <> sid, self())
        catch
          _, _ -> :error
        end

      # M-1.5: also register in Index 2 (`{sid, name}`) and Index 3
      # (`{sid, role}`) so `Esr.ActorQuery.find_by_name/2` and
      # `list_by_role/2` resolve this peer. Reuse the same string as
      # the Index 1 actor_id ("pty:<sid>") so cross-references stay in
      # sync. Best-effort guard: unit tests that bypass the application
      # supervisor (no IndexWatcher / no ETS tables) must keep working.
      _ =
        try do
          Esr.Entity.Registry.register_attrs("pty:" <> sid, %{
            session_id: sid,
            name: Map.get(args, :name) || ("pty-" <> sid),
            role: @role
          })
        catch
          _, _ -> :ok
        end
    end

    {:ok, state}
  end

  # No auto-confirm of trust / dev-channels dialogs: Phase 8 deleted
  # scripts/esr-cc.sh (superseded by Esr.Plugins.ClaudeCode.Launcher).
  # Launcher sets `--permission-mode auto` +
  # `--dangerously-load-development-channels`, and pre-trusts the
  # workspace path in `~/.claude.json`, so the trust dialog doesn't
  # render. The dev-channels dialog is answered by the operator in the
  # browser /attach page, or by the e2e helper for unattended scenarios.

  # ------------------------------------------------------------------
  # OSProcess lifecycle hooks (signatures match os_process.ex callbacks)
  # ------------------------------------------------------------------

  @impl Esr.OSProcess
  # Phase 8: esr-cc.sh deleted. Use Esr.Plugins.ClaudeCode.Launcher
  # when no explicit start_cmd override is present in state.
  # start_cmd override (from workspace config) is accepted as a string
  # and wrapped with bash for backwards-compatible operator escape hatch.
  def os_cmd(state) do
    case state.start_cmd do
      cmd when is_binary(cmd) and cmd != "" ->
        # Explicit override from workspace.start_cmd — wrap with bash
        # for legacy operator scripts that may still be strings.
        ["bash", "-c", cmd]

      _ ->
        # Default: Elixir-native Launcher builds the argv.
        Esr.Plugins.ClaudeCode.Launcher.spawn_cmd(
          cwd:  Map.get(state, :dir),
          role: Map.get(state, :workspace_role, "dev")
        )
    end
  end

  @impl Esr.OSProcess
  # Hand the configured `dir` (the per-session worktree path threaded
  # through from /new-session's slash → Session.New → Scope.Router)
  # to erlexec as the `:cd` option. Without this callback, OSProcess
  # falls back to BEAM's pwd, so the spawned claude process ends
  # up in `~/Workspace/esr/.../runtime` instead of
  # `<root>/.worktrees/<branch>` — confusing operators and breaking
  # the per-session git isolation PR-21θ promises.
  def os_cwd(state) do
    case state.dir do
      d when is_binary(d) and d != "" -> d
      _ -> nil
    end
  end

  @impl Esr.OSProcess
  def os_env(state) do
    case Map.get(state, :session_id) do
      sid when is_binary(sid) and sid != "" ->
        ws = Map.get(state, :workspace_name) || "default"
        chat_id = Map.get(state, :chat_id) || ""
        app_id = Map.get(state, :app_id) || ""

        chat_ids_json =
          Jason.encode!([%{chat_id: chat_id, app_id: app_id, kind: "feishu"}])

        # Terminal capability hints. claude's TUI emits a Device
        # Attributes query (`ESC[c`) at boot and **blocks** waiting for
        # the terminal to respond with its capabilities. Under iTerm
        # the shell answers immediately; under erlexec PTY launched
        # from launchd, no `TERM` is inherited (PR-21σ deliberately
        # does not source ~/.zshrc), so claude defaults to dumb mode
        # and stalls before printing its banner — observed live
        # 2026-05-01 with diagnostic raw-stdout logging. Pinning a
        # real terminal type plus reasonable size + UTF-8 locale
        # unblocks the boot path; xterm.js attaches later and
        # re-syncs the dimensions via the `resize` channel event.
        base = [
          {"ESR_SESSION_ID", sid},
          {"ESR_WORKSPACE", ws},
          {"ESR_CHAT_IDS", chat_ids_json},
          {"ESR_ESRD_URL", channel_ws_url()},
          {"TERM", "xterm-256color"},
          {"COLUMNS", "120"},
          {"LINES", "40"},
          {"LANG", "en_US.UTF-8"}
        ]

        # Phase 8: merge plugin config env (proxy vars, esrd_url override)
        # from Launcher. Build with the global-layer config; PtyProcess does
        # not carry user_uuid/workspace_id so global defaults apply.
        # Launcher.build_env returns a keyword list — convert to string tuples
        # and merge (base wins on key conflicts since we append).
        launcher_env =
          try do
            Esr.Plugins.ClaudeCode.Launcher.build_env(
              plugin_config: Esr.Plugin.Config.resolve("claude_code"),
              session_id: sid
            )
            |> Enum.map(fn {k, v} -> {Atom.to_string(k), v} end)
          rescue
            _ -> []
          end

        # Merge: launcher_env first so base-env explicit values win on conflict.
        launcher_env ++ base

      _ ->
        []
    end
  end

  @impl Esr.OSProcess
  # PR-22 raw-stdout hook — fires BEFORE line-splitting, so xterm.js
  # gets ANSI escape sequences intact across chunk boundaries.
  def on_raw_stdout(data, %{session_id: sid}) when is_binary(sid) and sid != "" do
    # DIAGNOSTIC (re-enabled 2026-05-02): TERM=xterm-256color didn't
    # unblock claude — observe the full PTY output stream + stdin
    # writes to find the real bottleneck. Remove once verified.
    digest =
      data
      |> :binary.bin_to_list()
      |> Enum.filter(&(&1 in 32..126 or &1 == 10))
      |> List.to_string()
      |> String.slice(0, 300)

    if digest != "" do
      require Logger
      Logger.info("pty_stdout sid=#{sid} bytes=#{byte_size(data)} digest=#{inspect(digest)}")
    end

    PubSub.broadcast(EsrWeb.PubSub, "pty:" <> sid, {:pty_stdout, data})
    :ok
  end

  def on_raw_stdout(_data, _state), do: :ok

  @impl Esr.OSProcess
  # PR-21ω'' carryover: any process exit (even 0) triggers peers
  # DynamicSupervisor's :transient restart so a fresh peer + claude
  # come back. New PTY's init re-registers Index 2/3 entries; siblings
  # find it via ActorQuery.list_by_role(sid, :pty_process).
  def on_os_exit(0, _state), do: {:stop, :pty_died_unexpectedly}
  def on_os_exit(status, _state), do: {:stop, {:pty_crashed, status}}

  @impl Esr.OSProcess
  # 1-arg per Esr.OSProcess @callback. No reason available; broadcast
  # bare :pty_closed so AttachLive can render the "session ended" overlay.
  def on_terminate(state) do
    case Map.get(state, :session_id) do
      sid when is_binary(sid) and sid != "" ->
        PubSub.broadcast(EsrWeb.PubSub, "pty:" <> sid, :pty_closed)

        # M-1.5: deregister Index 2/3 entries on graceful shutdown. The
        # IndexWatcher monitor still cleans up if we crash before reaching
        # this hook, so this call is purely an optimization that avoids
        # a stale-entry window between shutdown signal and DOWN delivery.
        _ =
          try do
            Esr.Entity.Registry.deregister_attrs("pty:" <> sid, %{
              session_id: sid,
              name: Map.get(state, :name) || ("pty-" <> sid),
              role: @role
            })
          catch
            _, _ -> :ok
          end

      _ ->
        :ok
    end

    :ok
  end

  # ------------------------------------------------------------------
  # Peer.Stateful — upstream events (line-split stdout) are no-ops
  # here; raw bytes are handled in on_raw_stdout/2 above.
  # ------------------------------------------------------------------

  @impl Esr.Entity.Stateful
  def handle_upstream(_msg, state), do: {:forward, [], state}

  # ------------------------------------------------------------------
  # Peer.Stateful — downstream events arrive when sibling peers send
  # us commands. M-2.4: removed :rewire_siblings handler (rewire deleted).
  # ------------------------------------------------------------------

  @impl Esr.Entity.Stateful
  def handle_downstream(_msg, state), do: {:forward, [], state}

  # ------------------------------------------------------------------
  # Helpers
  # ------------------------------------------------------------------

  defp name_for(%{session_name: n}), do: String.to_atom("esr_pty_#{n}")

  # Returns the esrd base WebSocket URL for cc_mcp's ESR_ESRD_URL env.
  # cc_mcp's ws_client.py appends `/channel/socket/websocket?vsn=2.0.0`
  # itself — we must return JUST the base (ws://host:port), no path.
  defp channel_ws_url do
    case Application.get_env(:esr, EsrWeb.Endpoint) do
      nil ->
        "ws://localhost:4001"

      cfg ->
        url = Keyword.get(cfg, :url, [])
        host = Keyword.get(url, :host, "localhost")
        port = Keyword.get(url, :port) || Keyword.get(cfg, :http, [])[:port] || 4001
        "ws://#{host}:#{port}"
    end
  end
end
